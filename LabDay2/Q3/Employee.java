public class Employee {
    String name, id, office, designation;

    public Employee() {
        this.name = "NULL";
        this.id = "NULL";
        this.office = "NULL";
        this.designation = "NULL";
    }
    public Employee(String name, String id, String office, String designation) {
        this.name = name;
        this.id = id;
        this.office = office;
        this.designation = designation;
    }

}
